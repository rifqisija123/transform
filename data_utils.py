def bersihkan_nama(nama):
    if pd.isna(nama):
        return ""
    return re.split(r",", str(nama))[0].strip()

def normalisasi_kabkot_columns(df):
    pola_kabkot = [
        r'kabupaten/?kota',
        r'kabupaten',
        r'kabupaten_kota',
        r'kabkot',
        r'kab\.?/?kota',
        r'kab.?kota',
        r'kota/kabupaten',
        r'kotakabupaten',
        r'kab. kota',
    ]

    for col in df.columns:
        col_bersih = col.lower().replace(" ", "").replace("_", "")
        for pola in pola_kabkot:
            if re.fullmatch(pola.replace(" ", ""), col_bersih):
                df.rename(columns={col: "kabkot"}, inplace=True)
                return df

    return df

